    def evaluate(self):

        input_text  = self.label.text()

        try:
                # Evaluasi input
                result = eval(input_text) #Fungsi ini digunakan untuk mengevaluasi input, input yang dievaluasi adalah input_text.
                result = round(result, 10) # Fungsi ini digunakan untuk membulatkan hasil evaluasi hingga 10 digit desimal.
                result = str(result) #Fungsi ini digunakan untuk mengubah hasil evaluasi yang sudah dikonversi menjadi tipe data Decimal menjadi tipe data string
                # Menampilkan hasil evaluasi
                self.label.setText(result)

        except Exception as e:
            # Jika input tidak valid, menampilkan pesan kesalahan
                self.label.setText("Error")  

